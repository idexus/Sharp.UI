using System.Text;
using Microsoft.CodeAnalysis;

namespace Sharp.UI.Generator.Extensions
{
    /// <summary>
    /// Pure emitter - takes an ExtensionModel and produces text.
    /// </summary>
    internal partial class ExtensionGenerator
    {
        readonly ExtensionModel model;
        StringBuilder builder;

        public ExtensionGenerator(ExtensionModel model)
        {
            this.model = model;
        }

        // -----------------------------------------------------------------

        public void Build(SourceProductionContext context)
        {
            if (!model.HasContent)
                return;

            builder = new StringBuilder();

            builder.AppendLine("//");
            builder.AppendLine("// <auto-generated> Sharp.UI.Generator.Extensions.Builder");
            builder.AppendLine("//");
            builder.AppendLine();
            builder.AppendLine("#nullable enable");
            builder.AppendLine("#pragma warning disable CS8601");
            builder.AppendLine();

            GenerateExtensionNameSpace();

            builder.AppendLine();
            builder.AppendLine("#pragma warning restore CS8601");
            builder.AppendLine("#nullable restore");

            context.AddSource(model.HintName, builder.ToString());
        }

        string GetUsingString() => model.IncludeSharpUIUsing ? "using Sharp.UI;\n" : "";

        void GenerateExtensionNameSpace()
        {
            builder.Append($@"
namespace {model.TargetNamespace}
{{
    using Sharp.UI.Internal;
    {GetUsingString()}
    public static partial class {model.ExtensionClassName}
    {{");

            foreach (var property in model.Properties)
                GeneratePropertyMethods(property);

            foreach (var @event in model.Events)
                GenerateEventMethods(@event);

            if (model.EmitITextAlignment)
                GenerateExtensionMethods_ITextAlignment();

            foreach (var property in model.InterfaceProperties)
                GeneratePropertyMethods(property);

            builder.AppendLine($@"
    }}
}}");
        }

        // -----------------------------------------------------------------
        // dispatcher - all decisions were already made in ExtensionModelBuilder
        // -----------------------------------------------------------------

        void GeneratePropertyMethods(PropertyModel p)
        {
            if (p.EmitValue) GenerateExtensionMethod_Value(p);
            if (p.EmitThickness) GenerateExtensionMethods_Thickness(p);
            if (p.EmitList) GenerateExtensionMethod_List(p, p.ListElementTypeName);
            if (p.EmitBindablePropertyBuilder) GenerateExtensionMethod_BindablePropertyBuilder(p);
            if (p.EmitSetters) GenerateExtensionMethod_Setters(p);
            if (p.EmitSettersThickness) GenerateExtensionMethod_Setters_Thickness(p);
            if (p.EmitSettersBuilder) GenerateExtensionMethod_SettersBuilder(p);
            if (p.EmitDataTemplate) GenerateExtensionMethod_DataTemplate(p);
            if (p.EmitGetValue) GenerateExtensionMethod_GetValue(p);
            if (p.AnimateTransformName != null) GenerateExtensionMethod_AnimateTo(p, p.AnimateTransformName);
        }

        // -----------------------------------
        // ----- value -----
        // -----------------------------------

        void GenerateExtensionMethod_Value(PropertyModel p)
        {
            if (model.IsSealed)
                builder.Append($@"
        public static {model.SymbolTypeName} {p.PropertyName}(this {model.SymbolTypeName} self,
            {p.PropertyTypeName} {p.CamelCaseName})
        {{
            {p.ValueAssignment}
            return self;
        }}
");
            else
                builder.Append($@"
        public static T {p.PropertyName}<T>(this T self,
            {p.PropertyTypeName} {p.CamelCaseName})
            where T : {model.SymbolTypeName}
        {{
            {p.ValueAssignment}
            return self;
        }}
");
        }

        // -----------------------------------
        // ----- thickness (Margin/Padding) -----
        // -----------------------------------

        void GenerateExtensionMethods_Thickness(PropertyModel p)
        {
            var bindable = $"{model.SymbolTypeName}.{p.PropertyName}Property";

            if (model.IsSealed)
                builder.Append($@"
        public static {model.SymbolTypeName} {p.PropertyName}(this {model.SymbolTypeName} self, double horizontalSize, double verticalSize)
        {{
            self.SetValue({bindable}, new Thickness(horizontalSize, verticalSize));
            return self;
        }}

        public static {model.SymbolTypeName} {p.PropertyName}(this {model.SymbolTypeName} self, double left, double top, double right, double bottom)
        {{
            self.SetValue({bindable}, new Thickness(left, top, right, bottom));
            return self;
        }}

#nullable disable
        public static {model.SymbolTypeName} {p.PropertyName}(this {model.SymbolTypeName} self, object _ = null, double left = 0, double top = 0, double right = 0, double bottom = 0)
        {{
            self.SetValue({bindable}, new Thickness(left, top, right, bottom));
            return self;
        }}
#nullable enable
");
            else
                builder.Append($@"
        public static T {p.PropertyName}<T>(this T self, double horizontalSize, double verticalSize)
            where T : {model.SymbolTypeName}
        {{
            self.SetValue({bindable}, new Thickness(horizontalSize, verticalSize));
            return self;
        }}

        public static T {p.PropertyName}<T>(this T self, double left, double top, double right, double bottom)
            where T : {model.SymbolTypeName}
        {{
            self.SetValue({bindable}, new Thickness(left, top, right, bottom));
            return self;
        }}

#nullable disable
        public static T {p.PropertyName}<T>(this T self, object _ = null, double left = 0, double top = 0, double right = 0, double bottom = 0)
            where T : {model.SymbolTypeName}
        {{
            self.SetValue({bindable}, new Thickness(left, top, right, bottom));
            return self;
        }}
#nullable enable
");
        }

        // -----------------------------------
        // ----- setters -----
        // -----------------------------------

        void GenerateExtensionMethod_Setters(PropertyModel p)
        {
            if (model.IsSealed)
                builder.Append($@"
        public static SettersContext<{model.SymbolTypeName}> {p.PropertyName}(this SettersContext<{model.SymbolTypeName}> self,
            {p.PropertyTypeName} {p.CamelCaseName})
        {{
            self.XamlSetters.Add(new Setter {{ Property = {p.BindablePropertyName}, Value = {p.CamelCaseName} }});
            return self;
        }}
");
            else
                builder.Append($@"
        public static SettersContext<T> {p.PropertyName}<T>(this SettersContext<T> self,
            {p.PropertyTypeName} {p.CamelCaseName})
            where T : {model.SymbolTypeName}
        {{
            self.XamlSetters.Add(new Setter {{ Property = {p.BindablePropertyName}, Value = {p.CamelCaseName} }});
            return self;
        }}
");
        }

        void GenerateExtensionMethod_Setters_Thickness(PropertyModel p)
        {
