        }}

#nullable disable
        public static SettersContext<T> {p.PropertyName}<T>(this SettersContext<T> self, object _ = default, double left = 0, double top = 0, double right = 0, double bottom = 0)
            where T : {model.SymbolTypeName}
        {{
            self.XamlSetters.Add(new Setter {{ Property = {p.BindablePropertyName}, Value = new Thickness(left, top, right, bottom) }});
            return self;
        }}
#nullable enable
");
        }

        // -----------------------------------
        // ----- get value (attached) -----
        // -----------------------------------

        void GenerateExtensionMethod_GetValue(PropertyModel p)
        {
            if (model.IsSealed)
                builder.Append($@"
        public static {p.PropertyTypeName} Get{p.PropertyName}Value<T>(this {model.SymbolTypeName} self)
        {{
            return ({p.PropertyTypeName})self.GetValue({p.BindablePropertyName});
        }}
");
            else
                builder.Append($@"
        public static {p.PropertyTypeName} Get{p.PropertyName}Value<T>(this T self)
            where T : {model.SymbolTypeName}
        {{
            return ({p.PropertyTypeName})self.GetValue({p.BindablePropertyName});
        }}
");
        }

        // -----------------------------------
        // ----- animate to -----
        // -----------------------------------

        void GenerateExtensionMethod_AnimateTo(PropertyModel p, string transformationName)
        {
            if (model.IsSealed)
                builder.Append($@"
        public static Task<bool> Animate{p.PropertyName}To(this {model.SymbolTypeName} self, {p.PropertyTypeName} value, uint length = 250, Easing? easing = null)");
            else
                builder.Append($@"
        public static Task<bool> Animate{p.PropertyName}To<T>(this T self, {p.PropertyTypeName} value, uint length = 250, Easing? easing = null)
            where T : {model.SymbolTypeName}");

            builder.Append($@"
        {{
            {p.PropertyTypeName} fromValue = self.{p.PropertyName};
            var transform = (double t) => Transformations.{transformationName}(fromValue, value, t);
            var callback = ({p.PropertyTypeName} actValue) => {{ self.{p.PropertyName} = actValue; }};
            return Transformations.AnimateAsync<{p.PropertyTypeName}>(self, ""Animate{p.PropertyName}To"", transform, callback, length, easing);
        }}
");
        }

        // -----------------------------------
        // ----- binding builders -----
        // -----------------------------------

        void GenerateExtensionMethod_BindablePropertyBuilder(PropertyModel p)
        {
            if (model.IsSealed)
                builder.Append($@"
        public static {model.SymbolTypeName} {p.PropertyName}(this {model.SymbolTypeName} self, Func<PropertyContext<{p.PropertyTypeName}>, IPropertyBuilder<{p.PropertyTypeName}>> configure)
        {{
            var context = new PropertyContext<{p.PropertyTypeName}>(self, {p.BindablePropertyName});
            configure(context).Build();
            return self;
        }}
");
            else
                builder.Append($@"
        public static T {p.PropertyName}<T>(this T self, Func<PropertyContext<{p.PropertyTypeName}>, IPropertyBuilder<{p.PropertyTypeName}>> configure)
            where T : {model.SymbolTypeName}
        {{
            var context = new PropertyContext<{p.PropertyTypeName}>(self, {p.BindablePropertyName});
            configure(context).Build();
            return self;
        }}
");
        }

        void GenerateExtensionMethod_SettersBuilder(PropertyModel p)
        {
            if (model.IsSealed)
                builder.Append($@"
        public static SettersContext<{model.SymbolTypeName}> {p.PropertyName}(this SettersContext<{model.SymbolTypeName}> self, Func<PropertySettersContext<{p.PropertyTypeName}>, IPropertySettersBuilder<{p.PropertyTypeName}>> configure)
        {{
            var context = new PropertySettersContext<{p.PropertyTypeName}>(self.XamlSetters, {p.BindablePropertyName});
            configure(context).Build();
            return self;
        }}
");
            else
                builder.Append($@"
        public static SettersContext<T> {p.PropertyName}<T>(this SettersContext<T> self, Func<PropertySettersContext<{p.PropertyTypeName}>, IPropertySettersBuilder<{p.PropertyTypeName}>> configure)
            where T : {model.SymbolTypeName}
        {{
            var context = new PropertySettersContext<{p.PropertyTypeName}>(self.XamlSetters, {p.BindablePropertyName});
            configure(context).Build();
            return self;
        }}
");
        }

        // -----------------------------------
        // ----- data template -----
        // -----------------------------------

        void GenerateExtensionMethod_DataTemplate(PropertyModel p)
        {
            if (model.IsSealed)
                builder.Append($@"
        public static {model.SymbolTypeName} {p.PropertyName}<T>(this {model.SymbolTypeName} self, System.Func<object> loadTemplate)
        {{
            {p.DataTemplateAssignment}
            return self;
        }}
");
            else
                builder.Append($@"
        public static T {p.PropertyName}<T>(this T self, System.Func<object> loadTemplate)
            where T : {model.SymbolTypeName}
        {{
            {p.DataTemplateAssignment}
            return self;
        }}
");
        }

        // -----------------------------------
        // ----- list -----
        // -----------------------------------

        void GenerateExtensionMethod_List(PropertyModel p, string elementTypeName)
        {
            var tail = p.PropertyTypeName.EndsWith("?") ? "?" : "";

            if (model.IsSealed)
                builder.Append($@"
        public static {model.SymbolTypeName} {p.PropertyName}(this {model.SymbolTypeName} self,
            IList<{elementTypeName}> {p.CamelCaseName})
        {{
            foreach (var item in {p.CamelCaseName})
                {p.AccessedWith}.{p.PropertyName}{tail}.Add(item);
            return self;
        }}

        public static {model.SymbolTypeName} {p.PropertyName}(this {model.SymbolTypeName} self,
            params {elementTypeName}[] {p.CamelCaseName})
        {{
            foreach (var item in {p.CamelCaseName})
                {p.AccessedWith}.{p.PropertyName}{tail}.Add(item);
            return self;
        }}
");
            else
                builder.Append($@"
        public static T {p.PropertyName}<T>(this T self,
            IList<{elementTypeName}> {p.CamelCaseName})
            where T : {model.SymbolTypeName}
        {{
            foreach (var item in {p.CamelCaseName})
                {p.AccessedWith}.{p.PropertyName}{tail}.Add(item);
            return self;
        }}

        public static T {p.PropertyName}<T>(this T self,
            params {elementTypeName}[] {p.CamelCaseName})
            where T : {model.SymbolTypeName}
        {{
            foreach (var item in {p.CamelCaseName})
                {p.AccessedWith}.{p.PropertyName}{tail}.Add(item);
            return self;
        }}
");
        }

        // -----------------------------------
        // ----- events -----
        // -----------------------------------

        void GenerateEventMethods(EventModel e)
        {
            if (model.IsSealed)
            {
                builder.Append($@"
        public static {model.SymbolTypeName} On{e.Name}(this {model.SymbolTypeName} self, {e.HandlerTypeName} handler)
        {{
            self.{e.Name} += handler;
            return self;
        }}
");
                builder.Append($@"
        public static {model.SymbolTypeName} On{e.Name}(this {model.SymbolTypeName} self, System.Action<{model.SymbolTypeName}> action)
        {{
            self.{e.Name} += (o, arg) => action(self);
            return self;
        }}
");
            }
            else
            {
                builder.Append($@"
        public static T On{e.Name}<T>(this T self, {e.HandlerTypeName} handler)
            where T : {model.SymbolTypeName}
        {{
            self.{e.Name} += handler;
            return self;
        }}
");
                builder.Append($@"
        public static T On{e.Name}<T>(this T self, System.Action<T> action)
            where T : {model.SymbolTypeName}
        {{
            self.{e.Name} += (o, arg) => action(self);
            return self;
        }}
");
            }
        }
    }
