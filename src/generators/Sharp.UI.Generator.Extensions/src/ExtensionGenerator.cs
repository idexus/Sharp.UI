            return self;
        }}

#nullable disable
        public static SettersContext<{model.SymbolTypeName}> {p.PropertyName}(this SettersContext<{model.SymbolTypeName}> self, object _ = default, double left = 0, double top = 0, double right = 0, double bottom = 0)
        {{
            self.XamlSetters.Add(new Setter {{ Property = {p.BindablePropertyName}, Value = new Thickness(left, top, right, bottom) }});
